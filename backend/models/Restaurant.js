const mongoose = require('mongoose');

const ItemSchema = new mongoose.Schema({
	sequence: {
		type: Number,
		required: true,
	},
	nameoffood: {
		type: String,
		require: true,
	},
	description: {
		type: String,
		require: true,
	},
	category: {
		type: String,
		require: true,
	},
	price: {
		type: String,
		require: true,
	},
	image: {
		type: String,
		require: true,
	},
	cloudinaryId: {
		type: String,
		require: true,
	},
	user: {
		type: mongoose.Schema.Types.ObjectId,
		ref: 'User',
	},
	createdAt: {
		type: Date,
		default: Date.now,
	},
});

module.exports = mongoose.model('Post', ItemSchema);
